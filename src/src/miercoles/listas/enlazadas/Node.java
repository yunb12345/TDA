package miercoles.listas.enlazadas;

public class Node {
    private int value;
    private Node next;

    public Node() {
        this.next = null;
    }

    public int getValue(){
        return value;
    }

    public Node getNext(){
        return next;
    }

    public int setValue(int value){
        return this.value = this.value;
    }

    public Node setNext(Node nuevo){
        return this.next = next;
    }
}
